package org.axiondb;

/**
 * A simple factory for creating {@link Index}instances.
 * 
 * @version  
 */
public interface IndexFactory {
    Index makeNewInstance(String name, Column col, boolean unique, boolean memorydb) throws AxionException;

    /**
     * Creates a new system index, with a unique internally-generated name, on the given
     * column with the given state of uniqueness. This index cannot be deleted via DML,
     * but must be deleted by its associated database object upon its own deletion.
     * 
     * @param table Table in which <code>col</code> resides
     * @param col Column to be indexed
     * @return new system-generated Index with an internally-generated name
     * @throws AxionException if error occurs during creation
     */
    Index makeNewSystemInstance(Table table, Column col, boolean memorydb) throws AxionException;
}
